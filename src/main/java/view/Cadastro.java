    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UsuarioBean novoUsuario = new UsuarioBean();
        novoUsuario.setNome(nomeTxt.getText().trim());
        novoUsuario.setUsuario(usuarioTxt.getText().trim());
        novoUsuario.setSenha(senhaTxt.getText().trim());

        if(novoUsuario.getUsuario().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha corretamente o campo Usuario!!", "ERRO!", JOptionPane.WARNING_MESSAGE);
        } else if (novoUsuario.getSenha().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha corretamente o campo Senha!!", "ERRO!", JOptionPane.WARNING_MESSAGE);
        } else if (novoUsuario.getNome().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha corretamente o campo Nome!!", "ERRO!", JOptionPane.WARNING_MESSAGE);
        } else {
            UsuarioDAO dao = new UsuarioDAO();
            dao.cadastrar(novoUsuario);
                new Login().setVisible(true);
                this.dispose();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

